    private void jButton_supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_supprimerActionPerformed
        // TODO add your handling code here:
        
        if(jTextField_idetd.getText().equals("")||jTextField_idCours.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Selectionner une ligne a supprimer  S.V.P!");

        }else{
            
          
            Score e=new Score();
            e.insererAjouterSupp('s', Integer.parseInt(jTextField_idetd.getText()),Integer.parseInt(jTextField_idCours.getText()), null, null);
          //  MaFonction mf=new MaFonction();
           // Principale.jLabel_nbcours.setText("Nombre du cours ="+mf.getNombreDonnees("cours"));
            vider();
            jTable1.setModel(new DefaultTableModel(null,new Object[]{"Id_Etudiant","Id_Cours","Score","Description"}));
            e.chargerScores(jTable1);}
        
                  
    }//GEN-LAST:event_jButton_supprimerActionPerformed
public boolean verifText(){
        if(jTextField_idetd.getText().equals("")||jTextField_idCours.getText().equals("")||jTextField_score.getText().equals("")||
                jTextField_description.getText().equals("")     ){
            JOptionPane.showMessageDialog(null, "Veuillez remplir tous les champs S.V.P!");
            return false;}
return true;}
